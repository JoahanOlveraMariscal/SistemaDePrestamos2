    private void nombreDeLosMaterialesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_nombreDeLosMaterialesItemStateChanged
        String nombreMaterial = nombreDeLosMateriales.getSelectedItem().toString();
        Material materialEscogido = new Material();
        for(int j=0; j<listaMateriales.size(); j++){
            if((listaMateriales.get(j).getNombre().equals(nombreMaterial))){
                materialEscogido = listaMateriales.get(j);
            }
        }
        if(materialEscogido.isUsaPotenciadores()){
            potenciadorDelMaterial.setEnabled(true);
            for(int i=0; i<listaPotenciadores.size(); i++){
                potenciadorDelMaterial.addItem(listaPotenciadores.get(i).toString());
            }
        }
        else{
            potenciadorDelMaterial.setEnabled(false);
        }
    }//GEN-LAST:event_nombreDeLosMaterialesItemStateChanged

